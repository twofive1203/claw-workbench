export type I18nKey = keyof typeof zhCnMessages

const MESSAGE_CATALOG: Record<AppLocale, Record<I18nKey, string>> = {
  'zh-CN': zhCnMessages,
  'en-US': enUsMessages,
}

const MESSAGE_PARAM_RE = /\{([a-zA-Z0-9_]+)\}/g

/**
 * 判断字符串是否为已注册的国际化 key。
 * @param key 候选 key。
 */
export function isI18nKey(key: string): key is I18nKey {
  return Object.prototype.hasOwnProperty.call(zhCnMessages, key)
}

/**
 * 按占位参数格式化消息模板。
 * @param template 消息模板。
 * @param params 占位参数。
 */
function formatI18nTemplate(template: string, params?: I18nParams): string {
  if (!params) return template
  return template.replace(MESSAGE_PARAM_RE, (_, rawName: string) => {
    const value = params[rawName]
    return value === null || value === undefined ? '' : String(value)
  })
}

/**
 * 按稳定 key 翻译界面文本。
 * @param key 国际化 key。
 * @param locale 目标语言。
 * @param params 占位参数。
 */
export function translateByKey(key: string, locale: AppLocale, params?: I18nParams): string | null {
  if (!isI18nKey(key)) return null
  const template = MESSAGE_CATALOG[locale][key] ?? zhCnMessages[key]
  return formatI18nTemplate(template, params)
}

