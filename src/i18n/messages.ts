/**
 * 规范化语言值。
 * @param raw 原始语言值。
 */
export function normalizeLocale(raw?: string | null): AppLocale {
  const value = (raw ?? '').toLowerCase()
  return value.startsWith('en') ? 'en-US' : 'zh-CN'
}

/**
 * 翻译单条界面文本。
 * @param text 原始中文文本。
 * @param locale 目标语言。
 */
export function translateUiText(text: string, locale: AppLocale): string {
  if (locale !== 'en-US') return text
  if (!text.trim()) return text

  const exactText = EXACT_TRANSLATIONS[text]
  if (exactText) return exactText

  let next = text
  for (const [source, target] of PHRASE_TRANSLATIONS) {
    if (!next.includes(source)) continue
    next = next.split(source).join(target)
  }
  return next
}
